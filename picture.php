<canvas id="obrazek" style="border: 2px solid;" width="600" height="600"></canvas>

<script>
    function rysujChoinke(ctx) {
        ctx.beginPath();
        ctx.moveTo(-25, -50);
        ctx.lineTo(-10, -80);
        ctx.lineTo(-20, -80);
        ctx.lineTo(-5, -110);
        ctx.lineTo(-15, -110);
        ctx.lineTo(0, -140);
        ctx.lineTo(15, -110);
        ctx.lineTo(5, -110);
        ctx.lineTo(20, -80);
        ctx.lineTo(10, -80);
        ctx.lineTo(25, -50);
        ctx.lineWidth = 2;
        ctx.lineJoin = 'round';
        ctx.strokeStyle = 'black';
        ctx.fillStyle = 'green';
        ctx.fill();
        ctx.fillStyle = 'brown';
        ctx.fillRect(-5, -50, 10, 20);
    }

    function rysujSlonce(ctx) {
        ctx.beginPath();
        ctx.arc(420, 70, 40, 0, Math.PI * 2);
        ctx.fillStyle = 'yellow';
        ctx.fill();
    }

    //kradziona chmura :)
    function rysujChmure(ctx) {
        ctx.beginPath();
        ctx.moveTo(170, 80);
        ctx.bezierCurveTo(130, 100, 130, 150, 230, 150);
        ctx.bezierCurveTo(250, 180, 320, 180, 340, 150);
        ctx.bezierCurveTo(420, 150, 420, 120, 390, 100);
        ctx.bezierCurveTo(430, 40, 370, 30, 340, 50);
        ctx.bezierCurveTo(320, 5, 250, 20, 250, 50);
        ctx.bezierCurveTo(200, 5, 150, 20, 170, 80);
        ctx.closePath();
        ctx.lineWidth = 5;
        ctx.fillStyle = '#8ED6FF';
        ctx.fill();
        ctx.strokeStyle = '#0000ff';
        ctx.stroke();
    }

    function rysujDomek(ctx) {

        //Dom
        ctx.fillStyle = 'purple';
        ctx.fillRect(50, 380, 120, 100);
        ctx.strokeStyle = 'grey';
        ctx.lineWidth = 4;
        ctx.strokeRect(50, 380, 120, 100);

        //Komin
        ctx.fillStyle = 'gray';
        ctx.fillRect(60, 310, 16, 50);
        ctx.lineWidth = 1;
        ctx.strokeStyle = 'black';
        ctx.strokeRect(60, 310, 16, 50);

        //Dach
        ctx.beginPath();
        ctx.moveTo(35, 380);
        ctx.lineTo(185, 380);
        ctx.lineTo(110, 300);
        ctx.closePath();
        ctx.lineWidth = 3;
        ctx.strokeStyle = 'grey';
        ctx.fillStyle = 'brown';
        ctx.fill();
        ctx.stroke();

        //Okna
        ctx.fillStyle = 'blue';
        ctx.fillRect(65, 400, 25, 35);
        ctx.lineWidth = 1;
        ctx.strokeStyle = 'black';
        ctx.strokeRect(65, 400, 25, 35);

        ctx.fillRect(130, 400, 25, 35);
        ctx.strokeRect(130, 400, 25, 35);

        //Ramki w oknach
        ctx.strokeStyle = 'black';
        ctx.beginPath();
        ctx.moveTo(77.5, 400);
        ctx.lineTo(77.5, 435);
        ctx.stroke();

        ctx.beginPath();
        ctx.moveTo(65, 417.5);
        ctx.lineTo(90, 417.5);
        ctx.stroke();

        ctx.beginPath();
        ctx.moveTo(142.5, 400);
        ctx.lineTo(142.5, 435);
        ctx.stroke();

        ctx.beginPath();
        ctx.moveTo(130, 417.5);
        ctx.lineTo(155, 417.5);
        ctx.stroke();

        //Drzwi
        ctx.fillStyle = 'brown';
        ctx.fillRect(97, 435, 25, 45);
        ctx.strokeStyle = 'black';
        ctx.strokeRect(97, 435, 25, 45);

        //Klamka
        ctx.beginPath();
        ctx.arc(116, 460, 2, 0, Math.PI * 2);
        ctx.fillStyle = 'green';
        ctx.fill();

    }

    function rysujDroge(ctx) {
        ctx.beginPath();
        ctx.lineWidth = 28;
        ctx.strokeStyle = 'brown';
        ctx.moveTo(120, 450);
        ctx.quadraticCurveTo(80, 550, 160, 550);
        ctx.quadraticCurveTo(200, 550, 270, 500);
        ctx.quadraticCurveTo(300, 480, 320, 400);
        ctx.quadraticCurveTo(350, 250, 450, 250);
        ctx.quadraticCurveTo(500, 250, 620, 270);
        ctx.stroke();
    }

    function rysuj() {
        const canvas = document.getElementById('obrazek');
        const ctx = canvas.getContext('2d');

        rysujSlonce(ctx);
        rysujDroge(ctx);
        rysujDomek(ctx);

        ctx.save();
        ctx.translate(200, 50);
        ctx.scale(0.5, 0.5);
        rysujChmure(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.translate(400, 40);
        ctx.scale(0.4, 0.4);
        rysujChmure(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.font = '30px Arial';
        ctx.fillStyle = 'purple';
        ctx.fillText('Wesolych Swiat', 170, 50);

        ctx.save();
        ctx.translate(130, 250);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();


        ctx.save();
        ctx.scale(1.3, 1.3);
        ctx.translate(300, 410);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.scale(1.4, 1.4);
        ctx.translate(350, 420);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.scale(1.4, 1.4);
        ctx.translate(235, 430);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.scale(1.1, 1.1);
        ctx.translate(75, 310);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.scale(1.2, 1.2);
        ctx.translate(250, 300);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.scale(1.2, 1.2);
        ctx.translate(200, 440);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();

        ctx.save();
        ctx.translate(200, 400);
        ctx.scale(1.1, 1.1);
        rysujChoinke(ctx);
        ctx.stroke();
        ctx.restore();
    }

    window.addEventListener("load", rysuj, true);
</script>